const Map: React.FC<MapProps> = ({ apiKey, center, locations, zoom }) => {
    const [markers, setMarkers] = useState(locations)
    const [markersCenter, setMarkersCenter] = useState(center)
    const searchParams = useSearchParams()

    useEffect(() => {
        const params = new URLSearchParams(searchParams)
        const locationsInURL = params.get("locationsVisited")
        if (locationsInURL) {
            const formattedLocations: google.maps.LatLngLiteral[] = JSON.parse(locationsInURL).map((location: string) => ({
                lat: Number(location.split(",")[0]),
                lng: Number(location.split(",")[1])
            }))
            setMarkers(formattedLocations)
            setMarkersCenter(findCenterCoordsOfRecap(undefined, JSON.parse(locationsInURL)))
        }
    }, [searchParams])


    return (
        <LoadScript googleMapsApiKey={apiKey}>
            <GoogleMap
                mapContainerStyle={{ width: '100%', height: '100%' }}
                center={markersCenter}
                zoom={zoom}
                options={mapOptions}
            >
                {markers.map((location, index) => (
                    <Marker
                        key={index}
                        position={location}
                    />
                ))}
            </GoogleMap>
        </LoadScript>
    );
};

export default Map;
